    private void jbtnObrisiCenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnObrisiCenuActionPerformed
        int red = jtblCena.getSelectedRow();
        if (red != -1) {

            mtc = (ModelTabeleCena) jtblCena.getModel();
            mtc.obrisiCenu(red);

        } else {

            JOptionPane.showMessageDialog(this, "Niste izabrali red koji zelite da obrisete");
            return;

        }


    }//GEN-LAST:event_jbtnObrisiCenuActionPerformed

