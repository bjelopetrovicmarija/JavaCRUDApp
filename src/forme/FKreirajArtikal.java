    private void jbtnSacuvajArtikalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajArtikalActionPerformed

        int artikalID = Integer.parseInt(jtxtIdArtikla.getText());
        String nazivArtikla = jtxtNaziv.getText();
        String opisArtikla = jtxtOpis.getText();
        ArrayList<Cena> cene = mtc.getLista();
        
        Artikal artikal=new Artikal(artikalID, nazivArtikla, opisArtikla, cene, 0);
        Kontroler.getInstanca().sacuvajArtikal(artikal);


    }//GEN-LAST:event_jbtnSacuvajArtikalActionPerformed

