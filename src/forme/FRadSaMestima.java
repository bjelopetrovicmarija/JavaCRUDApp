    private void srediTabelu() {

        ArrayList<Mesto> mesta = kontrolor.Kontroler.getInstanca().vratiMesta();
        mtm = new ModelTabeleMesto();
        if (mesta.size() > 0) {
            mtm.setLista(mesta);

        } else {

            System.out.println("Prazna lista");

        }
        jtblMesto.setModel(mtm);

    }
