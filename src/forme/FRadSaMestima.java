    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int red = jtblMesto.getSelectedRow();
        if (red != -1) {
            mtm = (ModelTabeleMesto) jtblMesto.getModel();

            Mesto mestoZaBrisanje = mtm.vratiMesto(red);
            int response = JOptionPane.showConfirmDialog(this, "Da li sigurno zelite da obrisete izabrano mesto?", "Brisanje mesta", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.OK_OPTION) {

                boolean uspesno=Kontroler.getInstanca().obrisiMesto(mestoZaBrisanje);
                if(uspesno){
                
                mtm.obrisi(red);
                
                
                }
            } else {

                return;
            }

        } else {

            JOptionPane.showMessageDialog(this, "Niste izabrali mesto koje zelite da obrisete!");

        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int red = jtblMesto.getSelectedRow();
        if (red != -1) {
            mtm=(ModelTabeleMesto) jtblMesto.getModel();
            Mesto izabranoMesto=mtm.vratiMesto(red);
            FIzmenaMesta fim = new FIzmenaMesta(this, true,izabranoMesto);
            fim.pack();
            fim.setVisible(true);

        } else {

            JOptionPane.showMessageDialog(this, "Niste izabrali mesto koje zelite da izmenite");
            return;

        }


    }//GEN-LAST:event_jButton2ActionPerformed

