    private void jbtnObrisiAdresuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnObrisiAdresuActionPerformed

        int red = jtblAdresa.getSelectedRow();
        if (red != -1) {

            mta = (ModelTabeleAdresaII) jtblAdresa.getModel();
            mta.obrisiAdresu(red);

        } else {

            JOptionPane.showMessageDialog(this, "Niste izabrali adresu koju zelite da obrisete");
            return;
        }


    }//GEN-LAST:event_jbtnObrisiAdresuActionPerformed

    private void jbtnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIzmeniActionPerformed

        String nazivUlice = jtxtNazivUlice.getText();
        String nazivMesta = jtxtNazivMesta.getText();
        mta = (ModelTabeleAdresaII) jtblAdresa.getModel();
        ArrayList<Adresa> adrese = mta.vratiSveAdrese();
        String dodatak = "";

        Ulica u = new Ulica(ulica.getUlicaID(), nazivUlice, nazivMesta, adrese, ulica.getMesto(), "update");
        if (!nazivMesta.equals(ulica.getNazivMesta())) {
            dodatak = ", nazivmesta='" + u.getNazivMesta() + "'";

        }
        Kontroler.getInstanca().izmeniUlicu(u, dodatak);


    }//GEN-LAST:event_jbtnIzmeniActionPerformed

