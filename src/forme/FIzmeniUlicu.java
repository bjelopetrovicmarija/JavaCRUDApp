    private void srediTabelu(Ulica ulica) {

        ArrayList<Adresa> lista = kontrolor.Kontroler.getInstanca().vratiAdreseZaUlicu(ulica);
        mta = (ModelTabeleAdresaII) jtblAdresa.getModel();
        mta.setLista(lista);

    }

    void dodajAdresu(int adresaID, int broj, String dodatakBroju) {
        UlicaBroj ulicaBroj = new UlicaBroj("MARIJA.ULICA_BROJ", broj, dodatakBroju);
        Adresa adresa = new Adresa(adresaID, ulicaBroj, ulica, "insert");
        mta = (ModelTabeleAdresaII) jtblAdresa.getModel();
        mta.dodajAdresu(adresa);

    }

    void izmeniAdresu(Adresa adresa) {

        int red = jtblAdresa.getSelectedRow();
        mta=(ModelTabeleAdresaII)jtblAdresa.getModel();
        mta.izmeni(red,adresa);

    }
