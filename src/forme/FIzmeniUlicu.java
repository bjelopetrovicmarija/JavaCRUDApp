    private void jbtnIzmeniAdresuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIzmeniAdresuActionPerformed

        int red = jtblAdresa.getSelectedRow();
        if (red != -1) {
            mta = (ModelTabeleAdresaII) jtblAdresa.getModel();
            Adresa a=mta.vratiAdresu(red);
            int broj=Integer.parseInt(JOptionPane.showInputDialog("Unesite broj"));
            String dodatakBroju=JOptionPane.showInputDialog("Unesite dodatak broju");
            UlicaBroj ulicaBroj=new UlicaBroj("MARIJA.ULICA_BROJ", broj, dodatakBroju);
            
            mta.setValueAt(broj, red, 0);
            mta.setValueAt(dodatakBroju, red, 1);
            
            
            Adresa izmenjena=new Adresa(a.getAdresaID(), ulicaBroj, ulica,"update");
            mta.izmeni(red, izmenjena);
            
          
        } else {

            JOptionPane.showMessageDialog(this, "Niste izabrali adresu koju zelite da izmenite");
            return;

        }


    }//GEN-LAST:event_jbtnIzmeniAdresuActionPerformed

